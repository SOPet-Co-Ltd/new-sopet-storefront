import type { Metadata } from 'next';
import { PolicyMarkdownLayout } from '../PolicyMarkdownLayout';
import { buildPolicyPageMetadata } from '../policy-metadata';

export const metadata: Metadata = buildPolicyPageMetadata({
  pathSegment: 'refund-policy',
  title: 'นโยบายการคืนเงิน',
  description:
    'เงื่อนไขการคืนเงินและคืนสินค้าสำหรับการสั่งซื้อผ่านแพลตฟอร์ม Sopet รวมถึงกรณีสินค้าชำรุด หมดอายุ และการคืนโดยไม่มีเหตุผล',
});

const REFUND_POLICY = `
อัปเดตครั้งล่าสุดเมื่อ: 4 เมษายน 2026

## 1. ขอบเขตของนโยบาย

นโยบายการคืนสินค้านี้ใช้กับการสั่งซื้อสินค้าทุกรายการผ่านแพลตฟอร์ม Sopet โดยมีเงื่อนไขการคืนสินค้าแบ่งออกเป็น 2 กรณี ดังนี้:

## 2. กรณีที่แพลตฟอร์มรับผิดชอบเต็มจำนวน (คืนเงิน 100%)

แพลตฟอร์ม Sopet จะดำเนินการคืนเงินเต็มจำนวนในกรณีดังต่อไปนี้เท่านั้น:

- สินค้าชำรุด / มีตำหนิ / ใช้งานไม่ได้ (Malfunctioned) — สินค้าที่ได้รับมีความเสียหาย ใช้งานไม่ได้ตามปกติ หรือไม่ตรงตามสเปคที่ระบุ
- สินค้าหมดอายุ (Expired) — สินค้าที่ได้รับมีวันหมดอายุผ่านไปแล้ว ณ วันที่จัดส่ง

## 3. กรณีคืนสินค้าโดยไม่มีเหตุผล (หักค่าการโอนเงิน 5%)

ในกรณีที่ลูกค้าต้องการคืนสินค้าโดยไม่มีสาเหตุจากตัวสินค้า (เช่น เปลี่ยนใจ ไม่ต้องการแล้ว) แพลตฟอร์มจะดำเนินการคืนเงินโดย หักค่าการโอน 5% ของยอดชำระ ก่อนคืนเงินส่วนที่เหลือให้ลูกค้า โดยต้องเป็นพัสดุที่ยังไม่มีการแกะทุกกรณี

## 4. หมายเหตุ

- การคืนสินค้าทุกกรณีต้องแจ้งผ่านระบบของแพลตฟอร์ม Sopet เท่านั้น
- ลูกค้าต้องส่งหลักฐาน เช่น รูปถ่ายสินค้า ในกรณีสินค้าชำรุดหรือหมดอายุ
- หากสินค้ามีการแกะหรือชำรุด จะไม่สามารถคืนได้ทุกกรณี
- แพลตฟอร์มขอสงวนสิทธิ์ในการพิจารณาและตัดสินใจขั้นสุดท้ายในทุกกรณี
`;

export default function RefundPolicyPage() {
  return <PolicyMarkdownLayout title="นโยบายการคืนเงิน" source={REFUND_POLICY} />;
}
